package com.revature.pojo;

public class Car {
	
	private String vin;
	private double price;

	public Car() {
		super();
	}

	public String getVin() {
		return vin;
	}

	public void setVin(String vin) {
		this.vin = vin;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

}
